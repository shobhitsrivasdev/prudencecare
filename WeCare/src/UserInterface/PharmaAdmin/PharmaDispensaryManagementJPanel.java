    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
       int selectedRow = supplyTable.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select a supply row!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        DrugSupplyWorkRequest req = (DrugSupplyWorkRequest)supplyTable.getValueAt(selectedRow, 0);
        String med = req.getMedicine();
        int supplyQ = req.getQuantity();
        Medicine medicineUpdate = null;
        for(Medicine m : enterprise.getMedicineCatalog().getMedicineList()){
            if(m.getName().equals(med)){
                medicineUpdate = m;
            }
        }
        if(medicineUpdate != null){
            int newQuant = (medicineUpdate.getQuantity()+supplyQ);
            medicineUpdate.setQuantity(newQuant);
            int demandNew =(medicineUpdate.getDemand()-supplyQ);
            if(demandNew < 0){
                medicineUpdate.setDemand(0);
            }else{
            medicineUpdate.setDemand(demandNew);
            }
            enterprise.getMedicineCatalog().updateMedicine(medicineUpdate);
        }
        account.getPharmaSupplyQueue().removeSupplyRequest(req);
        populateSupplyTable();
        JOptionPane.showMessageDialog(null, "Medicine Updated successfully!", "Information", JOptionPane.INFORMATION_MESSAGE);       
    }//GEN-LAST:event_jButton2ActionPerformed

