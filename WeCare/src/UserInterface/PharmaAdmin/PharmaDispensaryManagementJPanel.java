    public void populateSupplyTable(){
         DefaultTableModel model = (DefaultTableModel)supplyTable.getModel();
        model.setRowCount(0);
         for(DrugSupplyWorkRequest req: account.getPharmaSupplyQueue().getSupplyList()){
              Object row[] = new Object[5];
                 row[0] = req;
                 row[1] = req.getMedicine();
                 row[2] = req.getQuantity();
                 model.addRow(row);            
         }     
    }
    public void populateMedTable(){
        DefaultTableModel model = (DefaultTableModel)medTable.getModel();
        model.setRowCount(0);

         for(Medicine med: enterprise.getMedicineCatalog().getMedicineList()){
            Object row[] = new Object[5];
                 row[0] = med;
                 row[1] = med.getDosage();
                 row[2] = med.getPrice();
                 row[3] = med.getQuantity();
                 row[4] = med.getDemand();
                 model.addRow(row);                 
        }
    }
    public void populateMedQuoteBox(){
                    medQuoteBox.removeAllItems();
                    medQuoteBox.addItem("");
                    for(Medicine med :enterprise.getMedicineCatalog().getMedicineList()){
                        
                        if(med.getManufacturer() !=null){
                        medQuoteBox.addItem(med);
                        }
                    }
    }
        public void populateNewMedTable(){
              DefaultTableModel model = (DefaultTableModel)vaccineRequestTable.getModel();
        model.setRowCount(0);
        List<VaccineWorkRequest> requestList = account.getVaccineWorkQueue().getVaccineRequestList();
        for(VaccineWorkRequest req: requestList){

            Object row[] = new Object[5];
                 row[0] = req;
                 row[1] = req.getVaccine();
                 row[2] = req.getVaccine().getCondition();
                 row[3] = req.getResolveDate();          
                 model.addRow(row); 
            }
        
    
    }
    
